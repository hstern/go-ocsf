func TestStripHTMLAndWrap(t *testing.T) {
	in := "<p>The <em>foo</em> bar that is <strong>baz</strong>. <code>x</code> works.</p>"
	want := "The foo bar that is baz. x works."
	if got := collapseSpaces(stripHTML(in)); strings.TrimSpace(got) != want {
		t.Errorf("stripHTML+collapse = %q, want %q", got, want)
	}
	// Wrapping: a 50-char width should split a long sentence.
	long := "one two three four five six seven eight nine ten eleven twelve thirteen fourteen fifteen"
	lines := wrapAndStripHTML(long, 30)
	if len(lines) < 2 {
		t.Errorf("expected >=2 wrapped lines, got %d: %v", len(lines), lines)
	}
	for _, l := range lines {
		if len(l) > 30 && !strings.Contains(l, " ") {
			// A single un-splittable token may exceed width; that's fine.
			continue
		}
		if len(l) > 35 { // a small overshoot is acceptable on word boundary
			t.Errorf("line %q exceeds width target", l)
		}
	}
}
