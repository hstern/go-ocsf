func TestFieldGoType_PrimitivesAndArrays(t *testing.T) {
	s := &schema.Schema{Objects: map[string]schema.ObjectClass{}}
	cases := []struct {
		name    string
		attr    schema.ClassAttr
		want    string
		wantErr bool
	}{
		{
			name: "string scalar",
			attr: schema.ClassAttr{Name: "name", Type: "string_t"},
			want: "string",
		},
		{
			name: "string array",
			attr: schema.ClassAttr{Name: "tags", Type: "string_t", IsArray: true},
			want: "[]string",
		},
		{
			name: "json scalar",
			attr: schema.ClassAttr{Name: "raw", Type: "json_t"},
			want: "json.RawMessage",
		},
		{
			name: "object scalar present",
			attr: schema.ClassAttr{Name: "u", Type: "user"},
			want: "*User",
		},
		{
			name: "object array present",
			attr: schema.ClassAttr{Name: "us", Type: "user", IsArray: true},
			want: "[]User",
		},
		{
			name:    "object missing",
			attr:    schema.ClassAttr{Name: "g", Type: "ghost"},
			wantErr: true,
		},
		{
			name: "unknown type -> raw json",
			attr: schema.ClassAttr{Name: "x", Type: ""},
			want: "json.RawMessage",
		},
	}
	// Register the "user" object so the present cases succeed.
	s.Objects["user"] = schema.ObjectClass{Name: "user"}
	for _, c := range cases {
		t.Run(c.name, func(t *testing.T) {
			got, err := fieldGoType(s, c.attr, "objects")
			if c.wantErr {
				if err == nil {
					t.Fatalf("fieldGoType(%+v) = %q, want error", c.attr, got)
				}
				return
			}
			if err != nil {
				t.Fatalf("fieldGoType(%+v) error: %v", c.attr, err)
			}
			if got != c.want {
				t.Errorf("fieldGoType(%+v) = %q, want %q", c.attr, got, c.want)
			}
		})
	}
}

