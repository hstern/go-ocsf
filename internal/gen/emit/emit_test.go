func TestWriteObjectFile_StructureAndDocs(t *testing.T) {
	s := &schema.Schema{Objects: map[string]schema.ObjectClass{}}
	obj := schema.ObjectClass{
		Name:        "sample",
		Caption:     "Sample",
		Description: "A sample object used by tests.",
		Attributes: []schema.ClassAttr{
			{
				Name:        "name",
				Caption:     "Name",
				Description: "The sample's display name.",
				Type:        "string_t",
				Requirement: "required",
			},
			{
				Name:        "tags",
				Caption:     "Tags",
				Description: "Free-form labels.",
				Type:        "string_t",
				IsArray:     true,
				Requirement: "optional",
			},
			{
				Name:        "metadata",
				Caption:     "Metadata",
				Description: "Opaque per-vendor metadata.",
				Type:        "json_t",
				Requirement: "optional",
			},
		},
	}
	s.Objects["sample"] = obj

	buf := &bytes.Buffer{}
	if err := writeObjectFile(buf, s, obj); err != nil {
		t.Fatalf("writeObjectFile: %v", err)
	}
	out := buf.String()
	wants := []string{
		"// Code generated by internal/gen; DO NOT EDIT.",
		"package objects",
		`"encoding/json"`,
		"type Sample struct {",
		"Name string `json:\"name\"`",
		"Tags []string `json:\"tags,omitempty\"`",
		"Metadata json.RawMessage `json:\"metadata,omitempty\"`",
		"OCSF: name (type string_t, requirement required)",
		"OCSF: tags (type []string_t, requirement optional)",
		"OCSF: metadata (type json_t, requirement optional)",
	}
	for _, w := range wants {
		if !strings.Contains(out, w) {
			t.Errorf("writeObjectFile output missing %q\n--- output ---\n%s", w, out)
		}
	}
}

