// emitEnums writes one Go file per dictionary attribute that
// carries an enum. Files land under opts.OutDir/enums/ with one
// type per file (`type Severity int`), typed const block for
// each enum value (`const SeverityCritical Severity = 5`), and a
// String() method that returns the upstream caption (or the
// numeric value, prefixed, for codes not in the known set).
//
// Class-level enum extensions (e.g. authentication.activity_id
// adds Logon=1, Logoff=2 to the base activity_id's {0, 99}) are
// out of scope for this PR — those produce per-class typed
// enums alongside the class struct and can land additively once
// the shared types exist.
func emitEnums(opts Options) ([]string, error) {
	dir := filepath.Join(opts.OutDir, "enums")
	if err := os.MkdirAll(dir, 0o755); err != nil {
		return nil, fmt.Errorf("emit: mkdir %s: %w", dir, err)
	}

	names := make([]string, 0, len(opts.Schema.Dictionary.Attributes))
	for n, a := range opts.Schema.Dictionary.Attributes { //nolint:gocritic // copy fine in codegen path
		if len(a.Enum) == 0 {
			continue
		}
		names = append(names, n)
	}
	sort.Strings(names)

	var written []string
	for _, n := range names {
		attr := opts.Schema.Dictionary.Attributes[n]
		buf := &bytes.Buffer{}
		writeEnumHeader(buf, "enums", !isStringEnumType(attr.Type))
		if err := writeEnumType(buf, attr); err != nil {
			return nil, fmt.Errorf("emit enum %q: %w", n, err)
		}
		formatted, err := format.Source(buf.Bytes())
		if err != nil {
			return nil, fmt.Errorf("emit enum %q: format: %w\n--- pre-format ---\n%s", n, err, buf.String())
		}
		out := filepath.Join(dir, n+".go")
		if err := writeIfChanged(out, formatted); err != nil {
			return nil, fmt.Errorf("emit enum %q: write: %w", n, err)
		}
		written = append(written, out)
	}
	sort.Strings(written)
	return written, nil
}

// enumTypeName converts an OCSF enum-attribute name to a Go type
// identifier. The convention strips the `_id` / `_uid` suffix and
// applies the standard goName mangling: severity_id -> Severity,
// activity_id -> Activity, risk_level_id -> RiskLevel,
// class_uid -> Class, category_uid -> Category.
//
// Attribute names that don't end in _id or _uid (rare for enum
// attributes — none in 1.3.0) keep their full name; the
// suffix-strip is purely a readability sweetener.
func enumTypeName(attrName string) string {
	base := attrName
	switch {
	case strings.HasSuffix(base, "_id"):
		base = strings.TrimSuffix(base, "_id")
	case strings.HasSuffix(base, "_uid"):
		base = strings.TrimSuffix(base, "_uid")
	}
	return goName(base)
}

// enumConstName builds the Go const identifier for a single
// enum value: type-name prefix + a Pascal-cased version of the
// upstream caption with non-letter characters stripped. Captions
// like "In Progress" map to InProgress; numeric or
// punctuation-heavy captions fall back to the value's numeric
// id with a `Value` prefix so the const compiles.
func enumConstName(typeName, id, caption string) string {
	clean := sanitizeCaption(caption)
	if clean == "" {
		// Numeric fallback: SeverityValue99 for an unmapped caption.
		return typeName + "Value" + id
	}
	return typeName + clean
}

// sanitizeCaption Pascal-cases a caption for use in a Go
// identifier. Whitespace, hyphens, slashes, periods, and other
// punctuation split words; each word's first character
// capitalizes. Non-letter / non-digit runes are dropped. Empty
// input returns "".
func sanitizeCaption(s string) string {
	if s == "" {
		return ""
	}
	var b strings.Builder
	upNext := true
	for _, r := range s {
		switch {
		case unicode.IsLetter(r):
			if upNext {
				b.WriteRune(unicode.ToUpper(r))
				upNext = false
			} else {
				b.WriteRune(r)
			}
		case unicode.IsDigit(r):
			b.WriteRune(r)
			upNext = false
		default:
			upNext = true
		}
	}
	return b.String()
}

// writeEnumHeader writes the standard generated-file preamble
// for an enums file. Same shape as writeFileHeader but pulled
// out as a separate helper in case the enums package wants a
// future divergence (e.g. a different import block boilerplate).
func writeEnumHeader(w io.Writer, pkg string, needsStrconv bool) {
	_, _ = fmt.Fprintln(w, "// Code generated by internal/gen; DO NOT EDIT.")
	_, _ = fmt.Fprintln(w)
	_, _ = fmt.Fprintln(w, "// Copyright 2026 The go-ocsf Authors")
	_, _ = fmt.Fprintln(w, "// SPDX-License-Identifier: Apache-2.0")
	_, _ = fmt.Fprintln(w)
	_, _ = fmt.Fprintf(w, "package %s\n\n", pkg)
	if needsStrconv {
		_, _ = fmt.Fprintln(w, `import "strconv"`)
		_, _ = fmt.Fprintln(w)
	}
}

// writeEnumType emits the type declaration, the const block of
// enum values (sorted by integer id where applicable), and a
// String() method. Doc-comment carries the upstream caption +
// description for the enum as a whole, and per-const
// captions/descriptions for each value.
//
// The underlying Go type follows the attribute's dictionary
// type: numeric (integer_t/long_t) attributes get `type T int`;
// string-typed enums (only `depth` at v1.x) get `type T
// string`. Both share the same value-doc and String() shape;
// the const values are written as numeric literals or quoted
// strings accordingly.
func writeEnumType(w io.Writer, attr schema.DictAttr) error {
	typeName := enumTypeName(attr.Name)
	caption := strings.TrimSpace(attr.Caption)
	desc := strings.TrimSpace(attr.Description)
	if desc == "" {
		desc = caption
	}
	doc := typeName + " is the OCSF " + caption + " enum."
	if desc != "" {
		doc += " " + desc
	}
	for _, l := range wrapAndStripHTML(doc, 70) {
		_, _ = fmt.Fprintf(w, "// %s\n", l)
	}
	_, _ = fmt.Fprintf(w, "//\n// OCSF attribute: %s.\n", attr.Name)

	isString := isStringEnumType(attr.Type)
	if isString {
		_, _ = fmt.Fprintf(w, "type %s string\n\n", typeName)
	} else {
		_, _ = fmt.Fprintf(w, "type %s int\n\n", typeName)
	}

	ids := sortedEnumIDs(attr.Enum)

	_, _ = fmt.Fprintln(w, "const (")
	for _, id := range ids {
		v := attr.Enum[id]
		constName := enumConstName(typeName, id, v.Caption)
		if v.Description != "" {
			for _, l := range wrapAndStripHTML(constName+": "+v.Description, 64) {
				_, _ = fmt.Fprintf(w, "\t// %s\n", l)
			}
		}
		if isString {
			_, _ = fmt.Fprintf(w, "\t%s %s = %q\n", constName, typeName, id)
		} else {
			_, _ = fmt.Fprintf(w, "\t%s %s = %s\n", constName, typeName, id)
		}
	}
	_, _ = fmt.Fprintln(w, ")")
	_, _ = fmt.Fprintln(w)

	// String() returns the caption for known values; for unknown
	// values, numeric enums fall back to decimal text, string
	// enums fall back to the underlying string (cast).
	_, _ = fmt.Fprintf(w, "// String returns the OCSF caption for v, or the underlying\n")
	_, _ = fmt.Fprintf(w, "// value if v is outside the known set.\n")
	_, _ = fmt.Fprintf(w, "func (v %s) String() string {\n", typeName)
	_, _ = fmt.Fprintln(w, "\tswitch v {")
	for _, id := range ids {
		v := attr.Enum[id]
		constName := enumConstName(typeName, id, v.Caption)
		caption := v.Caption
		if caption == "" {
			caption = id
		}
		_, _ = fmt.Fprintf(w, "\tcase %s:\n\t\treturn %q\n", constName, caption)
	}
	_, _ = fmt.Fprintln(w, "\t}")
	if isString {
		_, _ = fmt.Fprintln(w, "\treturn string(v)")
	} else {
		_, _ = fmt.Fprintln(w, "\treturn strconv.Itoa(int(v))")
	}
	_, _ = fmt.Fprintln(w, "}")
	return nil
}

// isStringEnumType reports whether the attribute's dictionary
// type denotes a string-valued enum (only `depth` at v1.x).
// Numeric primitives (integer_t, long_t) and untyped attributes
// default to int.
func isStringEnumType(ocsfType string) bool {
	// Use primitiveGoType to resolve any string-derived primitive
	// (string_t, email_t, ...) to "string"; that's the marker
	// that the enum is a string enum.
	return primitiveGoType(ocsfType) == "string"
}

// sortedEnumIDs returns enum keys in numeric-then-string order:
// numeric ids ascend by value, with non-numeric ids appended in
// lex order. Stable across runs for codegen determinism.
func sortedEnumIDs(enum map[string]schema.EnumValue) []string {
	ids := make([]string, 0, len(enum))
	for id := range enum {
		ids = append(ids, id)
	}
	sort.Slice(ids, func(i, j int) bool {
		ai, aerr := strconv.Atoi(ids[i])
		bi, berr := strconv.Atoi(ids[j])
		if aerr == nil && berr == nil {
			return ai < bi
		}
		if aerr == nil {
			return true
		}
		if berr == nil {
			return false
		}
		return ids[i] < ids[j]
	})
	return ids
}
